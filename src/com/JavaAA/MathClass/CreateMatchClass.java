package com.JavaAA.MathClass;

public class CreateMatchClass {

    public static void main(String[] args) {
        //"Math" is the name of the class
        //the Math class also allows for you to invoke lots of methods (type "." to see them)
                           //here is the class, the method used here is absolute value
        System.out.println(Math.abs(-10));

        //this find the maximum number between the two values
        System.out.println(Math.max(3.6, 10.11));

        //this prints lowest number between the two
        System.out.println(Math.min(6,10));

        //this is used to calculate exponents (e.g., 5^2, 5^3)
        System.out.println(Math.pow(5, 3));

    }

}
