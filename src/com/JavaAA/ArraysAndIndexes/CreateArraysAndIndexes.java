package com.JavaAA.ArraysAndIndexes;

public class CreateArraysAndIndexes {

    public static void main(String[] args) {
        //this is an array
        int [] numbers = {2,0,1,4,100};
        //below statement allows you to get on of the number in the array (this example will get the number at index 4, which is "100")
        int getNumberFromArray = numbers[4];
        //below will print the 4th number in the array
        System.out.println(getNumberFromArray);

    }
}
